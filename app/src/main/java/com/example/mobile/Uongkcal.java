package com.example.mobile;

public class Uongkcal {
    int Canuong;
    int Dauong;
    int Cantap;
    int Datap;

    public Uongkcal(int canuong, int dauong, int cantap, int datap) {
        Canuong = canuong;
        Dauong = dauong;
        Cantap = cantap;
        Datap = datap;
    }

    public int getCanuong() {
        return Canuong;
    }

    public void setCanuong(int canuong) {
        Canuong = canuong;
    }

    public int getDauong() {
        return Dauong;
    }

    public void setDauong(int dauong) {
        Dauong = dauong;
    }

    public int getCantap() {
        return Cantap;
    }

    public void setCantap(int cantap) {
        Cantap = cantap;
    }

    public int getDatap() {
        return Datap;
    }

    public void setDatap(int datap) {
        Datap = datap;
    }
}
